<h2 class="title3">Block Board:</h2>
		<div style="width: 100%" >
			
		<p>Block Board is a wood based panel, made up of core of softwood stripes glued together, these stripes are about 28mm width and they are sandwiched between two layers of veneer (normally 4mm thick) under high pressure, the veneer can be of softwood, hardwood or MDF.</p>
		<div style="width:100%;float:left;"><div style="float: left; width: 60%"><p>The internal stripes are generally made of light weight poplar wood or spruce or pine wood. Block board comes in two grades MR (Moisture Resistant) and BWR (Boiling Water Resistant). They comply with Indian standard specifications IS: 1659: 2004.</p>
		<p>Block boards are lighter in weight, less expensive and offer very good resistance to bending; they are used as doors/shutters if the height of the shutter is more than 8 ft. Block board is stronger than MDF & Particle board.</p>

