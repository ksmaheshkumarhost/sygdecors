				</ul></li>
			<li>2.	BWR Grade
				<ul>
					<li>•	BWR grade stands for <b>Boiling Water Resistant </b>plywood (also called as Boiling Water Proof) can withstand up to 8 hrs in boiling water, this is ideal plywood used in Kitchens. The glue used to bind the ply sheets is formaldehyde.</li>
				</ul></li>
			</li>
		
