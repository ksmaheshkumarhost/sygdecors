		<ul>Types:
			<li>1.	LDF – Low Density Fibre Board - Density 400-00 Kg/m3</li>
			<li>2.	MDF- Medium Density Fibre Board – Density 600-800 Kg/m3</li>
			<li>3.	HDF - High Density Fibre Board - Density 700-1450 Kg/m3</li>
		</ul>
		<p>There are no ISI specifications for these fibre boards and there are no standard specifications on density and it differs brand to brand, it is still at the nascent stage in India</p>
