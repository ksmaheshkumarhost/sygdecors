<p><h2 class="title3">Plywood:</h2>
<p>Plywood is widely used in interior industry, these are thin sheets of wood glued together; it comes in many specifications based on the chemical treatment & glue used to bind the sheets. The specifications determine the type of use. Plywood is available from 2 mm to 38 mm thickness. </p>
<div style="width: 100%">
 
