<p><h2 class="title3">Wood Surface Finishing:</h2>
<p>Wooden finish gives the natural elegant touch, in order to maintain it, the finishing plays a vital role</p>
<p>They are basically three types of wooden surface finish</p>
<ul class="contentul">
	<li>French Polish</li>
	<li>Melamine Polish</li>
	<li>Polyurethane (PU) finish</li>
</ul>
<br />
<h2 class="title3">French Polish:</h2>
<p>French Polish is a not a material, it is a wood finishing technique by applying thin coats "Shellac" (resin secreted by the female lac bug) dissolved in ethyl alcohol, Although French polish is considered beautiful, it is prone to scratches  and stains easily, also not resistant to water. </p>
<br />
<h2 class="title3">Melamine Polish:</h2>
<p>The German word melamin was coined by combining the names of 2 other chemical products: Melam (a distillation derivative of ammonium thiocyanate) and Amine. Melamine is an organic compound that is often combined with formaldehyde to produce melamine resin, a synthetic polymer which is fire resistant and heat tolerant. Melamine resin is a very versatile material with a highly stable structure. Melamine can be easily molded while warm, but will set into a fixed form.</p>
<p>Melamine is water resistant, so the life is better than French polish</p>
<br />
<h2 class="title3">Polyurethane (PU) finish:</h2>
<p>Polyurethane is a synthetic varnish used for finishing and sealing the wood, PU is harder and more resistant to abrasion than its natural counterparts. PU is available in two types</p>
<ul class="contentul">
	<li>Water Based</li>
	<li>Oil Based</li>
</ul>
<p>Usage of water based or oil based depends on the finish, time and grain pattern required. Have a look at the picture, the left side (Lighter) water based PU is used and right side (Darker) Oil based PU is used. The middle portion is not varnished. </p>
