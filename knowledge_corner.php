				<ul>
					<li>•	 This plywood BWR grade ply coated with diammonium nitrogen phosphate and does not encourage spreading of flames and has the quality to retain strength for quite some time even under fire. They are used in railway pantry, malls, hotels and commercial kitchens.</li>
				</ul></li>
				
			<li><h4>C.	Marine Plywood: BIS IS 710:</h4>
				<ul>
